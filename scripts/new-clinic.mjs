function regenerateRegistry() {
  const files = fs
    .readdirSync(CLINICS_SRC)
    .filter((f) => f.endsWith(".ts") && !["types.ts", "index.ts", "registry.generated.ts"].includes(f))
    .map((f) => f.replace(/\.ts$/, ""))
    .sort();
  const imports = files.map((s) => `import { ${s} } from "./${s}";`).join("\n");
  const entries = files.map((s) => `  ${s},`).join("\n");
  const body = `// AUTO-GENERATED by scripts/new-clinic.mjs — do not edit by hand.
// Maps each clinic slug to its config. Regenerated whenever a clinic is added.
import type { ClinicConfig } from "./types";
${imports}

export const clinics: Record<string, ClinicConfig> = {
${entries}
};
`;
  fs.writeFileSync(path.join(CLINICS_SRC, "registry.generated.ts"), body);
  return files;
}

function envTemplate(c) {
  return `# Env for clinic "${c.slug}" (${c.nameEn}). Copy these into the deployment host.
NEXT_PUBLIC_CLINIC="${c.slug}"
DATABASE_URL="file:./${c.slug}.db"
NEXT_PUBLIC_SITE_URL="https://your-domain.example"
APP_URL="https://your-domain.example"

# Auth + worker secrets (generated — keep private)
AUTH_SECRET="${secret(48)}"
WA_AGENT_SECRET="${secret(48)}"

# WhatsApp
WHATSAPP_PROVIDER="waweb"
WHATSAPP_DEFAULT_CC="20"
NEXT_PUBLIC_CLINIC_WHATSAPP="${c.whatsapp}"
`;
}

