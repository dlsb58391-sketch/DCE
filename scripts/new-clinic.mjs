async function main() {
  const args = parseArgs(process.argv.slice(2));
  const interactive = Object.keys(args).length === 0;
  let rl;
  const get = async (key, q, def = "") => {
    if (args[key] != null) return args[key];
    if (!rl) rl = readline.createInterface({ input: process.stdin, output: process.stdout });
    return ask(rl, q, def);
  };

  const nameEn = await get("name-en", "Doctor/clinic name (EN)", "Dr. New Clinic");
  const nameAr = await get("name-ar", "Doctor/clinic name (AR)", nameEn);
  const slug = slugify(args.slug || (await get("slug", "URL slug", slugify(nameEn))));
  if (!slug) throw new Error("slug is required");

  const configPath = path.join(CLINICS_SRC, `${slug}.ts`);
  if (fs.existsSync(configPath) && !args.force) {
    throw new Error(`clinic "${slug}" already exists (src/lib/clinics/${slug}.ts). Use --force to overwrite.`);
  }

  const brandEn = await get("brand-en", "Short brand (EN)", nameEn);
  const brandAr = await get("brand-ar", "Short brand (AR)", nameAr);
  const roleEn = await get("role-en", "Role/specialty (EN)", "Cosmetic Dentist");
  const roleAr = await get("role-ar", "Role/specialty (AR)", "طبيب تجميل الأسنان");
  const whatsapp = (await get("whatsapp", "WhatsApp number (digits only, e.g. 2010...)", "")).replace(/\D/g, "");
  const phone = await get("phone", "Phone (display)", whatsapp ? `+${whatsapp}` : "");
  const email = await get("email", "Email", `info@${slug}.clinic`);
  const city = await get("city", "City", "Cairo");
  const photo = await get("photo", "Hero photo file (in public/)", "doctor-placeholder.png");
  if (rl) rl.close();

  const c = {
    slug,
    nameEn,
    name: { en: nameEn, ar: nameAr },
    brand: { en: brandEn, ar: brandAr },
    role: { en: roleEn, ar: roleAr },
    badge: { en: `100% Patient Recommended · ${roleEn}`, ar: `يوصي به ١٠٠٪ من المرضى · ${roleAr}` },
    subtitle: {
      en: `${roleEn} crafting natural, confident smiles — veneers, implants and complete smile makeovers.`,
      ar: `${roleAr} — نصمّم لك ابتسامة طبيعية وواثقة بالعدسات والزراعة وتجميل الابتسامة الكامل.`,
    },
    bio1: {
      en: `${nameEn} is a ${roleEn.toLowerCase()} dedicated to crafting natural, confident smiles — from veneers and smile design to implants and full-mouth rehabilitation.`,
      ar: `${nameAr} ${roleAr}، متخصص في تصميم الابتسامات الطبيعية والواثقة — من العدسات وتصميم الابتسامة إلى الزراعة وإعادة تأهيل الفم بالكامل.`,
    },
    photo: photo.startsWith("/") ? photo : `/${photo}`,
    whatsapp,
    phone,
    email,
    city,
    primary: "#a87f2b",
    primaryDark: "#876419",
    accent: "#d9b659",
  };

  // 1) config
  fs.writeFileSync(configPath, configTemplate(c));
  // 2) registry
  const all = regenerateRegistry();
  // 3) per-clinic folder
  const folder = path.join(ROOT, "clinics", slug);
  fs.mkdirSync(path.join(folder, "assets"), { recursive: true });
  fs.writeFileSync(path.join(folder, ".env"), envTemplate(c));
  fs.writeFileSync(path.join(folder, "DEPLOY.md"), deployReadme(c));
  fs.writeFileSync(path.join(folder, "clinic.json"), JSON.stringify(c, null, 2));

  console.log(`\n✅ Created clinic "${slug}" (${nameEn})`);
  console.log(`   • src/lib/clinics/${slug}.ts`);
  console.log(`   • registry now has: ${all.join(", ")}`);
  console.log(`   • clinics/${slug}/ (.env, DEPLOY.md, assets/)`);
  console.log(`\nNext:`);
  console.log(`   1. Put the hero photo at public${c.photo}`);
  console.log(`   2. Preview:  set NEXT_PUBLIC_CLINIC=${slug} && npm run dev`);
  console.log(`   3. Export:   node scripts/export-clinic.mjs ${slug}\n`);
}

main().catch((e) => {
  console.error("ERROR:", e.message);
  process.exit(1);
});
