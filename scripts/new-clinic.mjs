#!/usr/bin/env node
/**
 * new-clinic — scaffold a brand-new clinic (a new "account").
 *
 * It creates:
 *   1. src/lib/clinics/<slug>.ts        — the typed ClinicConfig (branding)
 *   2. regenerates registry.generated.ts (registers the new clinic)
 *   3. clinics/<slug>/.env              — per-clinic env (own DB + secrets)
 *   4. clinics/<slug>/assets/           — drop the hero photo / logo here
 *   5. clinics/<slug>/DEPLOY.md         — how to run/deploy this clinic
 *
 * After this, `node scripts/export-clinic.mjs <slug>` produces a standalone
 * deployable app folder in deploy/<slug>/.
 *
 * Usage (flags or interactive):
 *   node scripts/new-clinic.mjs --slug=sara --brand-en="Dr. Sara" --brand-ar="د. سارة" \
 *     --name-en="Dr. Sara Ali" --name-ar="د. سارة علي" --role-en="Cosmetic Dentist" \
 *     --role-ar="طبيبة تجميل الأسنان" --whatsapp=201000000000 --phone="+20 100 000 0000" \
 *     --email=info@drsara.clinic --city="Cairo" --photo=doctor-sara.png
 */
import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";
import readline from "node:readline";

const ROOT = process.cwd();
const CLINICS_SRC = path.join(ROOT, "src", "lib", "clinics");

function parseArgs(argv) {
  const out = {};
  for (const a of argv) {
    const m = /^--([^=]+)=(.*)$/.exec(a);
    if (m) out[m[1]] = m[2];
  }
  return out;
}

function ask(rl, q, def = "") {
  return new Promise((res) => rl.question(`${q}${def ? ` [${def}]` : ""}: `, (a) => res((a || def).trim())));
}

const slugify = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
const esc = (s) => String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
const secret = (n = 48) => crypto.randomBytes(n).toString("base64").replace(/[^a-zA-Z0-9]/g, "").slice(0, n);

